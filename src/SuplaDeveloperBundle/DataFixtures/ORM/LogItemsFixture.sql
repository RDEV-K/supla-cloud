DROP PROCEDURE IF EXISTS supla.generate_em_logs;

DELIMITER
$$
$$
CREATE PROCEDURE generate_em_logs(IN channelId INT)
BEGIN
  DECLARE
i INT DEFAULT 0;
  WHILE
i < 10000 DO
    INSERT IGNORE INTO `supla_em_log` VALUES (
      channelId,
      FROM_UNIXTIME(UNIX_TIMESTAMP('2014-01-01 01:00:00')+FLOOR(RAND()*31536000*7)),
      ROUND(RAND()*100,0),ROUND(RAND()*100,0),ROUND(RAND()*100,0),ROUND(RAND()*100,0),
      ROUND(RAND()*100,0),ROUND(RAND()*100,0),ROUND(RAND()*100,0),ROUND(RAND()*100,0),
      ROUND(RAND()*100,0),ROUND(RAND()*100,0),ROUND(RAND()*100,0),ROUND(RAND()*100,0),
      ROUND(RAND()*100,0),ROUND(RAND()*100,0)
    );
    SET
i = i + 1;
END WHILE;
END$$
DELIMITER ;

CALL generate_em_logs(73);
